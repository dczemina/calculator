// Math Functions

function add(a, b) {
    console.log(arguments);
    return a + b;
}
function subtract(a, b) {
    console.log(arguments);
    return a - b;
}
function multiply(a, b) {
    console.log(arguments);
    return a * b;
}
function divide(a, b) {
    console.log(arguments);
    return a / b;
}